public class Vector2 {
	public double x; // x value
	public double y; // y value
	
	public Vector2() {
		this.x = 0;
		this.y = 0;
	}
	
	
	public Vector2 (double x, double y) {
		this.x = x;
		this.y = y;
	}
}
